package q4;

public class students {
	String name;
	int rollno,mobileno, marks1,marks2,marks3,marks4, marks5;
	
	public students() {

	}

	public students(String name, int rollno, int mobileno, int marks1, int marks2, int marks3, int marks4, int marks5) {
		super();
		this.name = name;
		this.rollno = rollno;
		this.mobileno = mobileno;
		this.marks1 = marks1;
		this.marks2 = marks2;
		this.marks3 = marks3;
		this.marks4 = marks4;
		this.marks5 = marks5;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getRollno() {
		return rollno;
	}

	public int getMobileno() {
		return mobileno;
	}

	public void setMobileno(int mobileno) {
		this.mobileno = mobileno;
	}

	public void setRollno(int rollno) {
		this.rollno = rollno;
	}

	public int getMarks1() {
		return marks1;
	}

	public void setMarks1(int marks1) {
		this.marks1 = marks1;
	}

	public int getMarks2() {
		return marks2;
	}

	public void setMarks2(int marks2) {
		this.marks2 = marks2;
	}

	public int getMarks3() {
		return marks3;
	}

	public void setMarks3(int marks3) {
		this.marks3 = marks3;
	}

	public int getMarks4() {
		return marks4;
	}

	public void setMarks4(int marks4) {
		this.marks4 = marks4;
	}

	public int getMarks5() {
		return marks5;
	}

	public void setMarks5(int marks5) {
		this.marks5 = marks5;
	}
	
	
	
	
}
